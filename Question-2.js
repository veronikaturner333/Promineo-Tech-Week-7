//Create an array called names that contains the following values:
//'Sam', 'Tommy', 'Tim', 'Sally', 'Buck', 'Bob'
let names = ['Sam', 'Tommy', 'Tim', 'Sally', 'Buck', 'Bob']


//Use a loop to iterate through the array and calculate the average number of letters per name
//Declare variable for sum, "i" assumes value of each element in the array
//Adds together character length of each element until it reaches the end of the array
let nameSum = 0;
for (i=0; i < names.length; i++){
    nameSum += names[i].length;
}
//Declare variable for average, which is the sum of all the character lengths of each array
//element divided by the total amount of elements in the array
let  nameAv = nameSum/names.length;
//Expected Output: 3.8333333333333335
console.log(nameAv);


//Use a loop to iterate through the array again and concatenate all the names together,
//separated by spaces.
